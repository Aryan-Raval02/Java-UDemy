public class MathDemo 
{
    public static void main(String[] args) 
    {
        System.out.println(Math.abs(-123));
        
        System.out.println(Math.abs(123.56));

        System.out.println(Math.cbrt(27));

        System.out.println(StrictMath.abs(-12.34));

        System.out.println(Math.sqrt(4));

        System.out.println(Math.decrementExact(7));

        System.out.println(Math.getExponent(12.35324));

        System.out.println(Math.toRadians(90));

        System.out.println(Math.floorDiv(50, 9)); // it returns truncated results.

        System.out.println(Math.pow(2, 4));

        System.out.println(Math.max(10,20));
    }
}
